#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	unsigned int	nbr;

	nbr = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		nbr = -1;
	}
	nbr *= nb;
	if (nbr / 10)
		ft_putnbr(nbr / 10);
	ft_putchar('0' + nbr % 10);
}
