int	pick_arg(int arg[16], char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != 0)
	{
		if (15 < j)
			return (1);
		if (str[i] < '1' || '4' < str[i])
			return (1);
		if (str[i + 1] != 0 && str[i + 1] != ' ')
			return (1);
		arg[j] = str[i] - '0';
		if (j < 15)
			i++;
		i++;
		j++;
	}
	if (j != 16)
		return (1);
	return (0);
}
